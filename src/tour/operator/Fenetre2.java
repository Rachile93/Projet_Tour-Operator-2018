    private void jButton_enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_enregistrerActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd MM yyy");
        vm = new VoyageParVolModele();
        vbtm = new VoyageParBateauModele();

        String id = text_id_transport.getText();
        // String portDepart = getChoix("choisissez un numero :  ");
        // String portDestination = getChoix("choisissez un numero :  ");
        LocalTime heureDepart = LocalTime.parse(jTextField_Heure_depart.getText());
        LocalTime heureArrive = LocalTime.parse(jTextField_Heure_arrive.getText());
        LocalDate dateDepart = LocalDate.parse(jTextField_Date_depart.getText(), dtf);
        LocalDate dateArrive = LocalDate.parse(jTextField_Date_arrive.getText(), dtf);
        double prix = Double.parseDouble(jTextField_prix.getText());
        double prixSup = Double.parseDouble(jTextField_prix_sup.getText());
        String lieu_depart = jTextField_lieu_depart.getText();
        String lieu_arrive = jTextField_lieu_arrive.getText();
        if (jRadioButton_Vol.isSelected()) {
            //  name_lieu ="aeroport";
            String type_tranport = jRadioButton_Vol.getText();
            v_Vol = new VoyageParVol(id, lieu_depart, lieu_arrive, heureDepart, heureArrive, dateDepart, dateArrive, prix);
            vm.ajoutDeVol(v_Vol);
            ///jop3.showMessageDialog(null, "ligne insrer ", "Erreur", JOptionPane.INFORMATION_MESSAGE);

        }
        // name_lieu ="port";
        String type_tranport = jRadioButton_Bateau.getText();
        vBateau = new VoyageParBateau(id, lieu_depart, lieu_arrive, heureDepart, heureArrive, dateDepart, dateArrive, prix, prixSup);
        vbtm.ajouterVoyageBateau(vBateau);
    }//GEN-LAST:event_jButton_enregistrerActionPerformed

