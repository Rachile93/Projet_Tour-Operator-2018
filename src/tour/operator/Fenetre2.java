    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:

        Verification verif = new Verification();
        String id_lieu = text_id_lieu.getText();
        String nom = text_nom.getText();
        String ville = text_ville.getText();
        String pays = text_pays.getText();
        JOptionPane jop3 = new JOptionPane();
        aeroport = new AeroportModele();
        port = new PortModele();
        if (RadioAeroport.isSelected()) {
            if (verif.verifIdAeroport(id_lieu) == true) {
                if (verif.ifIdLieuExist(id_lieu) == false) {
                    a = new Aeroport(id_lieu, nom, ville, pays);
                    System.out.println(a);
                    aeroport.ajoutAeroport(a);
                    jop3.showMessageDialog(null, "ligne insrer ", "Erreur", JOptionPane.INFORMATION_MESSAGE);
                    text_id_lieu.setText("");
                    text_nom.setText("");
                    text_ville.setText("");
                    text_pays.setText("");

                } else {
                    jop3.showMessageDialog(null, "cette identifient existe deja ", "Erreur", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                //System.out.println("identitient aeroport incorect");
                jop3.showMessageDialog(null, "identifient aeroport incorect\nformat(AAA111)", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (verif.verifIdPort(id_lieu) == true) {
                if (verif.ifIdLieuExist(id_lieu) == false) {
                    p = new Port(id_lieu, nom, ville, pays);
                    System.out.println(p);
                    port.ajoutPort(p);
                    jop3.showMessageDialog(null, "ligne inserer", "Erreur", JOptionPane.INFORMATION_MESSAGE);
                    text_id_lieu.setText("");
                    text_nom.setText("");
                    text_ville.setText("");
                    text_pays.setText("");
                } else {
                    jop3.showMessageDialog(null, "cette identifient existe deja ", "Erreur", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                //  System.out.println("identitient port incorect");
                jop3.showMessageDialog(null, "identifient Port incorect\nformat(11AAA)", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_okActionPerformed

